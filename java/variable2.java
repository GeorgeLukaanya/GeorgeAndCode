public class variable2 {
    public static void main(String[] args){
        //integer types
        byte aSingleByte=100;//-128 to 127
        short aSmallNumber=20000;
        int anInteger=2147483647; 
        long aLongNumber=1223454343L;

        //decimal types
        double aDouble=1.79769313;
        float aFloat=3.4028F;

        //char
        char copyRightSymbol='%';

        System.out.println(copyRightSymbol);
        System.out.println(aSingleByte);
        System.out.println(aSmallNumber);
        System.out.println(anInteger);
        System.out.println(aLongNumber);
        System.out.println(aDouble);
        System.out.println(aFloat);

    }
    
}
